package com.krawler.calendar.calendarmodule;

import java.util.Date;

public class DeskeraCalendar {
	public static final int CAL_GENERAL=0;
	public static final int CAL_ACTIVITY=1;
	public static final int CAL_DEFAULT=2;
	
    private String id;
    private String name;
    private String description;
    private String location;
    private String timeZone;
    private String colorCode;
    private int type;
    private Date createdOn;
    private Date updatedOn;
    private boolean denyDelete;
    
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getTimeZone() {
		return timeZone;
	}
	public void setTimeZone(String timeZone) {
		this.timeZone = timeZone;
	}
	public String getColorCode() {
		return colorCode;
	}
	public void setColorCode(String colorCode) {
		this.colorCode = colorCode;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public Date getCreatedOn() {
		return createdOn;
	}
	public void setCreatedOn(Date createdOn) {
		this.createdOn = createdOn;
	}
	public Date getUpdatedOn() {
		return updatedOn;
	}
	public void setUpdatedOn(Date updatedOn) {
		this.updatedOn = updatedOn;
	}
	public boolean isDenyDelete() {
		return denyDelete;
	}
	public void setDenyDelete(boolean denyDelete) {
		this.denyDelete = denyDelete;
	}
}
